public class CommunicationThread extends Thread {

    private ServerThread serverThread;
    private Socket socket;
    public String link = "https://pokeapi.co/api/v2/pokemon/";

    public CommunicationThread(ServerThread serverThread, Socket socket) {
        this.serverThread = serverThread;
        this.socket = socket;
    }

    @Override
    public void run() {
        if (socket == null) {
            Log.e(Constants.TAG, "[COMMUNICATION THREAD] Socket is null!");
            return;
        }
        try {
            BufferedReader bufferedReader = Utilities.getReader(socket);
            PrintWriter printWriter = Utilities.getWriter(socket);
            if (bufferedReader == null || printWriter == null) {
                Log.e(Constants.TAG, "[COMMUNICATION THREAD] Buffered Reader / Print Writer are null!");
                return;
            }
            Log.i(Constants.TAG, "[COMMUNICATION THREAD] Waiting for parameters from client (pokeName / information type!");
            String pokeName = bufferedReader.readLine();
            if (pokeName == null || pokeName.isEmpty()) {
                Log.e(Constants.TAG, "[COMMUNICATION THREAD] Error receiving parameters from client (pokeName / information type!");
                return;
            }

            Log.i(Constants.TAG, "[COMMUNICATION THREAD] Getting the information from the webservice...");
            HttpClient httpClient = new DefaultHttpClient();
            HttpGet httpGetXKCD = new HttpGet(link + pokeName);
            ResponseHandler<String> responseHandler = new BasicResponseHandler();
            String pageSourceCode = null;
            try {
                pageSourceCode = httpClient.execute(httpGetXKCD, responseHandler);
            } catch (ClientProtocolException clientProtocolException) {
                Log.e(Constants.TAG, clientProtocolException.getMessage());
                if (Constants.DEBUG) {
                    clientProtocolException.printStackTrace();
                }
            } catch (IOException ioException) {
                Log.e(Constants.TAG, ioException.getMessage());
                if (Constants.DEBUG) {
                    ioException.printStackTrace();
                }
            }

            JSONObject jObject = new JSONObject(pageSourceCode);
            JSONArray jsonArray = jObject.getJSONArray("abilities");

            String sb = jsonArray.getJSONObject(0).getJSONObject("ability").getString("name");
            String sb2 = jsonArray.getJSONObject(1).getJSONObject("ability").getString("name");

            String url1 = jsonArray.getJSONObject(0).getJSONObject("ability").getString("url");
            String url2 = jsonArray.getJSONObject(1).getJSONObject("ability").getString("url");


            printWriter.println(sb+sb2 + " " + url1 + " " + url2);
            printWriter.flush();


        } catch (IOException ioException) {
            Log.e(Constants.TAG, "[COMMUNICATION THREAD] An exception has occurred: " + ioException.getMessage());
            if (Constants.DEBUG) {
                ioException.printStackTrace();
            }
        } catch (JSONException jsonException) {
            Log.e(Constants.TAG, "[COMMUNICATION THREAD] An exception has occurred: " + jsonException.getMessage());
            if (Constants.DEBUG) {
                jsonException.printStackTrace();
            }
        } finally {
            if (socket != null) {
                try {
                    socket.close();
                } catch (IOException ioException) {
                    Log.e(Constants.TAG, "[COMMUNICATION THREAD] An exception has occurred: " + ioException.getMessage());
                    if (Constants.DEBUG) {
                        ioException.printStackTrace();
                    }
                }
            }
        }
    }

}
